package com.exploudont.tamagotchi;

/**
 * Define the Tamagotchi's Status.
 */
public enum Status {
	/**
	 * If the sickness of the tamagotchi has a good value.
	 */
	HEALTHY,
	
	/**
	 * If the sickness of the tamagotchi has a bad value.
	 */
	SICK,

	/**
	 * If the sickness of the tamagotchi has a very bad value.
	 */
	VERY_SICK,
	
	/**
	 * If the tamagotchi is die.
	 */
	DIE
}